      <h3 class="second">Why am I getting different speeds between my computer and my phone/tablet?</h3>
      <ol type="i">
      <li class="li1">Speedtest is measuring your real-time network connection, so tests taken within a few minutes of each other might vary a little based on network congestion and available bandwidth. If your Speedtest results are significantly different, make sure that you’re:<br>
             a.Testing the same connection. If one device is on Wi-Fi and the other is not, you’re testing the speeds of different connections.<br>
             b.Testing to the same server. Speedtest automatically selects a server to test to based on ping, but you can also select a server to test to.<br>
          <li class="li1">Also, note that there are large variations in Wi-Fi and cellular radio quality and MIMO stream handling quality between devices. These variations can cause a device to deliver slower test results than another device or computer. 
